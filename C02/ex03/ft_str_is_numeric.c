#include <stdio.h>

int	ft_str_is_numeric(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0' && (str[c] >= '0' && str[c] <= '9'))
	{
		c++;
	}
	return (str[c] == '\0');
}
