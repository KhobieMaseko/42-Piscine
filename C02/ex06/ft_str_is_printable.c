#include <stdio.h>

int	ft_str_is_printable(char *str)
{
	int	c;

	c = 0;
	while (str[c] != '\0' && (str[c] >= 32 && str[c] <= 127))
	{
		c++;
	}
	return (str[c] == '\0');
}
