#include <stdio.h>

char	*ft_strcpy(char *dest, char *src)
{
	int		count;

	count = 0;
	if (dest == NULL)
	{
		return (NULL);
	}
	while (src[count] != '\0')
	{
		dest[count] = src[count];
		count++;
	}
	dest[count] = '\0';
	return (dest);
}
