#include <stdio.h>

int	ft_is_prime(int nb)
{
	int	c;

	c = 2;
	if (nb == 0 || nb == 1 || nb < 0)
	{
		return (0);
	}
	while (c <= nb / 2)
	{
		if (nb % c == 0)
		{
			return (0);
		}
		else
		{
			c++;
		}
	}
	return (1);
}
